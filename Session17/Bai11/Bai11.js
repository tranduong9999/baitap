console.log(Math.abs(-5.5)); // Kết quả: 5.5, vì giá trị tuyệt đối của -5.5 là 5.5
console.log(Math.abs(10));   // Kết quả: 10, vì giá trị tuyệt đối của 10 vẫn là 10

console.log(Math.ceil(4.4)); // Kết quả: 5, vì 4.4 làm tròn lên thành 5
console.log(Math.ceil(5.8)); // Kết quả: 6, vì 5.8 làm tròn lên thành 6

console.log(Math.floor(5.7)); // Kết quả: 5, vì 5.7 làm tròn xuống thành 5
console.log(Math.floor(6.1)); // Kết quả: 6, vì 6.1 làm tròn xuống thành 6

console.log(Math.round(10.1)); // Kết quả: 10, vì 10.1 gần với 10 hơn
console.log(Math.round(20.6)); // Kết quả: 21, vì 20.6 gần với 21 hơn
